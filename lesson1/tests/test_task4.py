from lesson1.task_D import main


def test_beat_by_figure():
    input_data_1 = [
        "********",
        "********",
        "*R******",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_1) == 49

    input_data_2 = [
        "********",
        "********",
        "******B*",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_2) == 54

    input_data_3 = [
        "********",
        "*R******",
        "********",
        "*****B**",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_3) == 40

    input_data_4 = [
        "R*******",
        "R*******",
        "R*******",
        "R*******",
        "R*******",
        "R*******",
        "R*******",
        "R*******",
    ]
    assert main(input_data_4) == 0

    input_data_5 = [
        "*******B",
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_5) == 56

    input_data_6 = [
        "B*******",
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
        "B******B",
    ]
    assert main(input_data_6) == 48

    input_data_7 = [
        "********",
        "********",
        "*******R",
        "****R***",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_7) == 64 - 15 - 13

    input_data_8 = [
        "*R******",
        "**R*****",
        "***R****",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_8) == 64 - 15 - 13 - 11

    input_data_9 = [
        "R" * 8,
        "R" * 8,
        "R" * 8,
        "R" * 8,
        "B" * 8,
        "B" * 8,
        "B" * 8,
        "B" * 8,
    ]
    assert main(input_data_9) == 0

    input_data_10 = [
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_10) == 64

    input_data_11 = [
        "********",
        "**R*****",
        "***B****",
        "********",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_11) == 64 - 15 - 8

    input_data_12 = [
        "********",
        "**R*****",
        "***B****",
        "****B***",
        "********",
        "********",
        "********",
        "********",
    ]
    assert main(input_data_12) == 64 - 15 - 8 - 5
